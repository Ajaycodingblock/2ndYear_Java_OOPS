// Program 2: Check if a number is Even or Odd
public class EvenOdd {
    public static void main(String[] args) {
        int num = 17;

        // Check remainder
        if (num % 2 == 0) {
            System.out.println(num + " is Even");
        } else {
            System.out.println(num + " is Odd");
        }
    }
}
